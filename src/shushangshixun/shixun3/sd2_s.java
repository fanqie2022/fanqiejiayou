package shushangshixun.shixun3;

public class sd2_s {
    public static void main(String[] args){
        int k = 1;
        while (k <= 50){
            System.out.print(k + " ");
            if (k % 5 == 0){
                    System.out.print("\n");
            }
            k++;
        }
    }
}
