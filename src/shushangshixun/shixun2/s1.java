package shushangshixun.shixun2;

public class s1 {
    public static void main(String[] args) {
        int a = (int) 45.23;
        long b = (long) 456.6F;
        char c = (char) 97.14;
        System.out.println("45.23强制转换成int的结果: " + a);
        System.out.println("456.6F强制转换成long的结果: " + b);
        System.out.println("97.14强制转换成char的结果: " + c);
    }
}
